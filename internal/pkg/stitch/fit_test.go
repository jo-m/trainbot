func genTestSeq(dx []int) sequence {
	t0 := time.Now()

	seq := sequence{startTS: &t0}
	for i, dx := range dx {
		seq.frames = append(seq.frames, &image.RGBA{})
		seq.dx = append(seq.dx, dx)
		seq.ts = append(seq.ts, t0.Add(time.Second*time.Duration(i+1)))
	}
	return seq
}

func sum(val []int) int {
	var ret int
	for _, v := range val {
		ret += v
	}
	if ret < 0 {
		return -ret
	}
	return ret
}

